/**
 * 부위 선택 → 심평원 통계 조회
 * @param {string} bodyPart - "무릎" | "허리" | "어깨" | "고관절"
 * @param {string} region   - "서울" | "경기" | "부산" | ...
 * @returns {object} stats object
 */
function getHiraStats(bodyPart, region) {
  const data = HIRA_DATA[bodyPart];
  if (!data) return null;

  const regionPts = data.by_region[region]?.환자수 || 0;
  const surgPts   = data.surgery_by_region[region] || 0;
  const inst      = data.institution;
  const total     = data.stats.total_patients;

  return {
    label:            data.label,
    dept:             data.dept,
    region_patients:  regionPts,
    national_patients: total,
    surgery_rate_pct: data.stats.surgery_rate_pct,
    no_surgery_rate_pct: data.stats.no_surgery_rate_pct,
    surgery_patients_region: surgPts,
    clinic_ratio_pct: inst['의원']?.비율 || 0,
    hospital_ratio_pct: inst['병원']?.비율 || 0,
    general_ratio_pct: inst['종합병원']?.비율 || 0,
    clinic_annual_cost: data.cost_summary.clinic_annual_avg,
    hospital_annual_cost: data.cost_summary.hospital_annual_avg,
    surgery_avg_cost: data.cost_summary.surgery_avg,
    first_visit_range: data.cost_summary.first_visit_range,
  };
}

/**
 * Claude AI 프롬프트 컨텍스트 생성
 * @param {string} bodyPart
 * @param {string} region
 * @param {string} age
 * @param {string} sex
 * @param {string} worry  - 환자 주요 걱정
 * @returns {string} prompt string
 */
function buildAIPrompt(bodyPart, region, age, sex, worry) {
  const s = getHiraStats(bodyPart, region);
  if (!s) return '';
  return `당신은 심평원 데이터를 기반으로 환자에게 진료 여정을 안내하는 따뜻한 의료 정보 도우미입니다.

[환자 정보]
- ${age} ${sex} / ${region} 거주
- 증상 부위: ${s.label}
- 가장 걱정되는 것: ${worry}

[심평원 실제 통계 — 2024년]
- ${region} 지역 유사 환자수: ${s.region_patients.toLocaleString()}명
- 전국 환자수: ${s.national_patients.toLocaleString()}명
- 기관 선택 비율: 의원 ${s.clinic_ratio_pct}% / 병원 ${s.hospital_ratio_pct}% / 종합병원 ${s.general_ratio_pct}%
- 수술 전환율: ${s.surgery_rate_pct}% (100명 중 ${Math.round(s.surgery_rate_pct)}명 수준)
- 수술 안 받는 비율: ${s.no_surgery_rate_pct}%
- 의원 평균 연간 진료비: ${s.clinic_annual_cost.toLocaleString()}원
- 추천 진료과: ${s.dept}

위 통계를 바탕으로:
1. 환자의 걱정에 먼저 공감해주세요
2. 비슷한 환자들의 패턴을 친근하게 설명해주세요
3. 수술 가능성을 솔직하되 안심되게 말해주세요
4. 지금 당장 해야 할 첫 행동 하나를 알려주세요
(200자 내외, 마지막 줄에 통계 기반 안내임을 짧게 명시)`;
}

